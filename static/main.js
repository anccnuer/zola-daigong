function switchon() {
    const menu = document.getElementById('menu');
    const on = document.getElementById('switch-on');
    menu.style.display = 'block';
    on.style.display = 'none';
}

function switchoff() {
    const menu = document.getElementById('menu');
    const on = document.getElementById('switch-on');
    menu.style.display = 'none';
    on.style.display = 'block';
}

